    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Cliente cliente = new Cliente();
        int row = tabla.getSelectedRow();
        String nombre = (String) tabla.getValueAt(row, 1);

        try {
            cliente = hotel.getServicioCliente().getClienteByName(nombre);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }

        VistaCliente vistaCliente = new VistaCliente(cliente, vistaHotel);

        VistaPrincipal.getInstance().setPanel(vistaCliente);
    }//GEN-LAST:event_jButton3ActionPerformed

