    private void cargarClientes(){
        List<Cliente> clientes = hotel.getServicioCliente().getClientes();
        for(Cliente cliente: clientes){
            comboBoxClientes.addElement(cliente.getNombre());
        }
    }
    
    private void cargarHabitaciones(){
        List<Habitacion> habitaciones = hotel.getServicioHabitacion().getHabitaciones();
        for(Habitacion habitacion: habitaciones){
            if(habitacion.isDisponible()){
                comboBoxHabitaciones.addElement(""+habitacion.getNumeroHabitacion());
                ocuparBtn.setEnabled(true);
            }
        }
    }
    
    private void cargarHuespedes(){
        if(habitacion != null){
            List<Huesped> huespedes = habitacion.getHuespedes();
            listaHuespedes.removeAllElements();
            for(Huesped huesped: huespedes){
                listaHuespedes.addElement(huesped.getNombre());
            }
        }
    }
    
    private void buscarHabitacionSeleccionada(){
        String numeroHabitacion = jComboBox2.getSelectedItem().toString();
         try {
            habitacion = hotel.getServicioHabitacion().buscar(Integer.parseInt(numeroHabitacion));
        } catch (Exception ex) {
           JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }

