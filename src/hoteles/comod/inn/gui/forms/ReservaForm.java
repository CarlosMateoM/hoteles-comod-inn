    private void ocuparBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ocuparBtnActionPerformed
        // TODO add your handling code here:
        Window window = SwingUtilities.getWindowAncestor(ReservaForm.this);
        
        String clienteName = jComboBox1.getSelectedItem().toString();
        try {
            cliente = hotel.getServicioCliente().getClienteByName(clienteName);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
        
        buscarHabitacionSeleccionada();
        
        DetalleCuenta detalleCuenta = new DetalleCuenta("renta habitacion " + habitacion.getTipoHabitacion(), habitacion.getPrecio());
        
        Cuenta cuenta = new Cuenta();
        
        cuenta.registrar(detalleCuenta);
        
        cliente.getServicioCuenta().agregarDetalleCuenta(cuenta);
        
        hotel.getServicioHabitacion().cambiarDisponibilidadHabitacion(habitacion.getNumeroHabitacion(), false);
        window.dispose();
    }//GEN-LAST:event_ocuparBtnActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        Huesped huesped = new Huesped();
        
        String numeroHabitacion = jComboBox2.getSelectedItem().toString();
        huesped.setNumeroHabitacion(Integer.parseInt(numeroHabitacion));
        
        Dialog dialog = new Dialog(null, true, new ClienteForm(huesped));
        dialog.setVisible(true);
        
        buscarHabitacionSeleccionada();
        
        habitacion.agregarHuesped(huesped);
        cargarHuespedes();
    }//GEN-LAST:event_jButton2ActionPerformed


